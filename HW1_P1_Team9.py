#Convert Celsius to Fahrenheit
celsius = int(input("Enter a number in degrees celsius: "))
fahrenheit = (9/5) * celsius + 32

#Display Fahrenheit temperature
print("Temperature in Fahrenheit is %s degrees\n" % fahrenheit)